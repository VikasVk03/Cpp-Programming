//  learn about horizontal and vertical tab
//  horizontal tab --> \t
// vertical tab --> \v

#include <iostream>

int main()
{
    using namespace std;

    cout << "HORIZONTAL TAV (\\t) DEMO\n";
    cout << "Name\tAge\tCity\n";
    cout << "Vikas\t21\tMumbai\n";
    cout << "Aman\t22222\tDelhi\n";
    cout << "Manoj\t23\tJaipur\n";

    cout << "\n---------------------\n\n";

    cout << "VERTICAL TAB (\\v) DEMO \n";
    cout << "LIne 1\vLine 2\vLine 3\n";
    cout << "After vertical tabs\n";

    return 0;
}